const itemCategory = document.querySelector(".items"); 
const btnContainer = document.querySelector(".header");

window.addEventListener("DOMContentLoaded", function () {
  displayMenuItems(main_menu);
  displayMenuButtons();
});

function displayMenuItems(menuItems) {
  let displayMenu = menuItems.map(function (item) {
    return `<div class="mess">
        <div class="food">
            <img src="${item.img}" alt="${item.title}">
        </div>
        <div class="about">
            <div class="item_name">${item.title}</div>
            <div class="cost">${item.price}</div>
        </div>
        <div class="foodabout">
            <p>${item.about}</p>
        </div>
    </div>`;
  });

  displayMenu = displayMenu.join(""); 
  itemCategory.innerHTML = displayMenu;
}

function displayMenuButtons() {
  const categories = main_menu.reduce(
    function (values, item) {
      if (!values.includes(item.category)) {
        values.push(item.category);
      }
      return values;
    },
    ["all"]
  );

  const categoryBtns = categories
    .map(function (category) {
      return `<button class="filter-btn" data-id="${category}">
          ${category.toUpperCase()}
        </button>`;
    })
    .join("");

  btnContainer.innerHTML = categoryBtns;

  const filterBtns = btnContainer.querySelectorAll(".filter-btn");

  filterBtns.forEach(function (btn) {
    btn.addEventListener("click", function (e) {
      const category = e.currentTarget.dataset.id;
      const menuCategory = main_menu.filter(function (menuItem) {
        if (category === "all") {
          return true; 
        }
        return menuItem.category === category;
      });
      displayMenuItems(menuCategory);
    });
  });
}

